pub fn get_data_kana() -> Vec<Vec<&'static str>> {
    let mut result = vec![];

    let kana = DATA_JP_KANA.to_vec();
    for k in kana {
        result.push(k.to_vec());
    }

    let kana_dakuten = DATA_JP_KANA_WITH_DAKUTEN.to_vec();
    for kd in kana_dakuten {
        result.push(kd.to_vec());
    }

    return result;
}
